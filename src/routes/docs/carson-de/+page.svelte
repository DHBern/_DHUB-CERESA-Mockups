<p>
	<span class="font-bold">Carson McCuller<insEditorial>s</insEditorial></span><br data-type="lf" />
	Eine junge amerikanische Schriftstellerin setzt sich durch.<br data-type="lf" />
	<br data-type="lf" />
</p>
<p>
	<a data-person href="https://explore.gnd.network/gnd/118575724" target="_blank"
		>Carson McCullers</a
	>
	ist heute bestimmt die meistgelesene und meistübersetzte junge amerikanische Schriftstellerin. Im Staate
	<a data-place href="https://explore.gnd.network/gnd/4020273-2" target="_blank">Georgia</a> im
	Jahre 1917 geboren, schrieb sie zwischen zwanzig und siebenundzwanzig Jahren drei
	aufsehenerregende Romane:
	<a data-work href="https://explore.gnd.network/gnd/1217441484" target="_blank"
		>«The Heart is a lonely Hunter»</a
	>,
	<a data-work href="https://explore.gnd.network/gnd/1143730216" target="_blank"
		>«Reflections in a golden Eye»</a
	>
	und
	<a data-work href="https://explore.gnd.network/gnd/4313598-5" target="_blank"
		>«The Member of the Wedding»</a
	>. Alle drei erschienen in der französischen Uebersetzung bei
	<a data-publisher href="" target="_blank">Stock</a>
	– und der letzte, unter dem Titel <a data-work href="" target="_blank">«Frankie Addams»</a>, erst
	kürzlich – und fanden, man kann den Ausdruck wahrheitsgetreu benutzen, in Frankreich eine
	begeisterte Aufnahme. Dies, trotzdem die junge Autorin sich kaum zu den in Frankreich so beliebten
	Auseinandersetzungen über die Problematik amerikanischer und europäischer Roman-Literatur (sprich
	«recherches romanesques») verwerten lässt. Wir wissen, biographisch gesprochen, nicht viel von
	<a data-person href="https://explore.gnd.network/gnd/118575724" target="_blank"
		>Carson McCullers</a
	>; rare Photographien zeigen uns das blasse, sehr tragische und anderseits fast kindliche Gesicht
	dieser überaus begabten Schriftstellerin, die bereits ihr erstes Buch in meisterhafter
	Beherrschung von Stil und Ausdruck schrieb, und von deren Büchern man sagt, sie seien grausam und
	eines Faulkners würdig. Ohne sich in eine solche etwas simplistische «Abfertigung» der
	Sonderlichkeit
	<a data-person href="https://explore.gnd.network/gnd/118575724" target="_blank"
		>Carson McCullers</a
	>
	zu verirren, kann man doch nicht umhin, eine gewisse Wahrheit darin zu erkennen. Tatsächlich zeichnen
	sich alle drei oben erwähnten Romane durch ihren erbarmungslos objektiven und kalt aufgebauten Gang
	aus; und vielleicht gibt er ihnen jenen Anschein von Grausamkeit – denn umsonst würde man sie, die
	Grausamkeit, in der Auswahl und der Behandlung des «Inhalts» suchen. Cocteau sagte einmal: «Der Stil,
	ist die Gangart des Geschriebenen»; demnach also wäre zu sagen,
	<a data-person href="https://explore.gnd.network/gnd/118575724" target="_blank"
		>Carson McCullers</a
	>
	Stil sei grausam. Und somit wäre das Thema der Grausamkeit erschöpft, wie auch das Thema «Faulkner»
	in bezug auf McCullers Werke. Behandeln sie doch alle drei, die Romane, ein einziges und stets anders
	bearbeitetes aber wiederzuerkennendes Thema: das Irren, das unruhige, einsame Drängen des menschlichen
	Herzens. So wie es der Titel des Erstlingswerkes auf eine erschütternde Art sagt: «Das Herz ist ein
	einsamer Jäger.» So folgen die Romanfiguren jenen unterirdischen Pfaden, und ihre Worte, und ihre Taten
	alle sind nur der im täglichen Leben davon sichtbare Widerschein. Mick, das junge noch nicht ausgewachsene
	Mädchen, schleicht sich in den langen Sommerabenden jenen Häusern entlang, aus deren Fenster es Musik
	ertönen hört, auf der Suche nach einer Melodie, in der es sich selbst finden könnte und die das Namenlose
	ausdrückt, das es bedrängt. Der blasse, taubstumme Bursche, der eigentlich im Mittelpunkt des Buches
	steht, vergibt sich vollkommen in den Erklärungen, Bewegungen seiner Hände, die er immer und immer
	wieder, ohne je erfahren zu können, ob der andere ihn versteht, vor den abwesenden Augen seines ebenfalls
	taubstummen Freundes beginnt. Und alle anderen, die vielen bunten Figuren des Buches, stehen um ihn
	herum und sprechen zu ihm, der nicht Antwort geben kann, von dem was ihr Herz und ihre Einsamkeit erfüllt.
	Es ist an[?] sich erschütternd zu denken, dass ein zweiundzwanzigjähriges Mädchen diese von menschlichem
	Elend und tiefster Einsamkeit lebende Geschichte erfinden konnte. Demnach schiene die junge amerikanische
	Literatur anderen, uns allen näheren Problemen zu entspringen, als die der ihr vorhergehenden Generation.<br
		data-type="lf"
	/>
	«Reflections in a Golden Eye» scheint ein viel mehr «literarisches» Werk zu sein; die Geschichte ist
	auf eine absichtlich künstliche Art erzählt. Die Personen, in einer wahrhaftig «psychoanal[y]tischen
	Beleuchtung» als reine Komparsen behandelt, werden mit fester Hand zu dem an sich banalen Ergebnis
	eines Mordfalles geführt. Trotzdem enthält und umfasst gerade dieses «ausgeklügelte» Buch in seinen
	wichtigsten Zügen die ganze Macht von
	<a data-person href="https://explore.gnd.network/gnd/118575724" target="_blank"
		>Carson McCullers</a
	>
	Erzählungskunst. Zusammenfassend und aufschlussgebend nicht nur über die Mittel und Zwecke seiner Autorin,
	sondern auch und hauptsächlich über eine Zeit und ihre wichtigsten moralischen und psychologischen
	Gegebenheiten. Von «The Member of the Wedding» wurde kürzlich in Manhattan eine von der Autorin selbst
	verfasste Bühnenbearbeitung gegeben. Es geht hier um die weltfremde, törichte, traurige Backfischjahrezeit.
	Mit welcher Sorgfalt, mit welcher Liebe zeichnet Carson M[cC]ullers die Welt und das Wesen der Frankie
	Jasmine Addams, dieses zu hochgewachsenen zwölfjährigen Mädchens, dessen Sehnsüchte um so inniger sind,
	als sie zwecklos, sinnlos in Phantasiegebilden und Unmöglichem herumirren.<br data-type="lf" />
	Tatsächlich, wenn Carson M[cC]ullers, ihre Bücher und ihre Art, Schreiben und Leben aufzufassen, das
	«junge Amerika» vertreten, dann berührt uns dieses junge Amerika eindringlicher, als das von gestern.<br
		data-type="lf"
	/>
	Und den Schweizer Leser mag es berühren, dass die Originalfassung des Bandes «Reflections in a golden
	Eye» die Widmung trägt: «To Annemarie Clarac-Schwarzenbach.» <br data-type="lf" />
</p>
